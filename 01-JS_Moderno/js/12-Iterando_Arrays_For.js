// Destructuring de arrays
const tecnologias = ['HTML', 'CSS', 'JavaScript', 'React', 'NodeJS'];

// Imprimir los elementos del arreglo de manera indivisible a trav√©s de su indice
console.log(tecnologias[0]); // Imprime: "HTML"
console.log(tecnologias[1]); // Imprime: "CSS"
console.log(tecnologias[2]); // Imprime: "JavaScript"
console.log(tecnologias[3]); // Imprime: "React"
console.log(tecnologias[4]); // Imprime: "NodeJS"

// Para iterar un arreglo, podemos usar un bucle for, su sintaxis es:
for (let i = 0; i < tecnologias.length; i++) {
    console.log(tecnologias[i]);
}